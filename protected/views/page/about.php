<h1>MkLst</h1>

<p>MkLst.com is dead simple lists you can print and save.  You can edit them anonymously, you can protect them, you can share them.</p>
<p>The idea is to trim the fat down to something you will use 100% of the features on.</p>

<h1>Why?</h1>

<p>To scratch my own itch. I wanted lists I could print (I know, horrible, right?) and reorder quickly.</p>

<p>The reason this project is so "complicated" is that I also wanted to try writing my own MVC framework, which I did and stuffed into this project.</p>

