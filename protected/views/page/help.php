<h2>Creating A List</h2>

<p>
	Creating a list is easy, you only have to give it a name.  There are other options of course, and they are:
  <ul>
    <li>
      <b>Your Email</b><br/>
      If you set this you will get notices when things happen to your lists. You can also recover passwords or even get a list of all the lists you've made.
    </li>
    <li>
      <b>View Password</b><br/>
      Without this set, anyone can view your list (if they can find it).
    </li>
    <li>
      <b>Edit Password</b><br/>
      Without this set, anyone can edit your list (if they can find it).
    </li>
    <li>
      <b>Delete Password</b><br/>
      Without this set, anyone can permanently delete your list (if they can find it).
    </li>
  </ul>
</p>

<p>
  Please note that it's not really very "dangerous" not to set passwords on your lists unless you are publishing the link somewhere.  List ID's are very large and unpredictable, so they aren't going to be guessed somehow. Additionally, anyone who goes trolling for list ID's and guesses too many wrong gets locked out of the system. For good. Consider this your warning.
</p>

<h2>Editing A List</h2>

<h3>Inserting Items</h3>
<p>
	<img src="<?php echo uri::path( 'img/help/add-item.png' ); ?>" /><br/>
	Click the "Add Item" button or type "n" to create a new item. Then just fill it in and press "enter".
</p>

<h3>Editing Items</h3>
<p>
	<img src="<?php echo uri::path( 'img/help/edit-item-b.png' ); ?>" /><br/>
	Click on the item you want to edit, change it as you see fit and hit "enter".
</p>

<h3>Deleting Items</h3>
<p>
	<img src="<?php echo uri::path( 'img/help/edit-item.png' ); ?>" /><br/>
	Click on the item you want to delete, remove all the text in the input box and hit "enter".
</p>

<h3>Reordering Items</h3>
<p>
	<img src="<?php echo uri::path( 'img/help/reorder-items.png' ); ?>" /><br/>
	Click and drag the item you want to where you want it. Release.
</p>

